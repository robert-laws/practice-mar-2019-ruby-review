# control flow
# handled by operators if, else, and elsif in a ruby program

name = "bob"
age = 23

# if statement
if name == "bob"
  puts "it's bob"
end

# else keyword
if name == "hal"
  puts "it's hal"
else
  puts "it's someone else"
end

# elsif statement
if name == "kal"
  puts "it's kal"
elsif name == "mel"
  puts "it's mel"
else
  puts "it's someone else"
end
